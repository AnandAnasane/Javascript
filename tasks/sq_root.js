const b = Math.sqrt(a); 
console.log(`The Square root of ${a} is ${b}`); //output: The Square root of 8 is 2.8284271247461903

const c = 8
const d = parseInt(Math.sqrt(c));
console.log(`The Square root of ${c} is ${d}`); //output: The Square root of 8 is 2
