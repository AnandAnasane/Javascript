
// Arrow Function
// Arrow functions provide a shorthand syntax for defining functions.
// They do not have their own this or arguments.
const add = (p, q) => p+q;
console.log(add(5,7));


//simple function
let a = 100;
let b = 20;
function addition()
{
    return a + b ;
}

console.log(addition());

//with using parameters
function ad(c, d) {
    return c + d; // Return the result
}
console.log(ad(10, 20)); // Output: 30

